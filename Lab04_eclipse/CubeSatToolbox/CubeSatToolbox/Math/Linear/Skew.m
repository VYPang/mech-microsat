function s = Skew( v )

%% Converts a vector into a skew symmetric matrix.
%--------------------------------------------------------------------------
%   Form:
%   s = Skew( v )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   v                  Vector
%
%   -------
%   Outputs
%   -------
%   s                  Skew symmetric matrix
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright (c) 1999 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------

s = [    0 -v(3)  v(2);...
      v(3)    0  -v(1);...
     -v(2)  v(1)     0];


