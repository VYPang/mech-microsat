function z = CosD( x )

%% Cosine with input in degrees
%
%   Since version 1.
%--------------------------------------------------------------------------
%   Form:
%   z = CosD( x )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   x                  Argument (deg)
%
%   -------
%   Outputs
%   -------
%   z                  Cos(x)
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%	Copyright (c) 1993-2004 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------

z = cos( rem(x,360)*pi/180 );


