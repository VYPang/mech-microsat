function s = SinD ( a )

%% Sine with input in degrees
%
%   Since version 1.
%--------------------------------------------------------------------------
%   Form:
%   s = SinD ( a )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   a                Argument (deg)
%
%   -------
%   Outputs
%   -------
%   s                Sine(a)
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%	Copyright (c) 1993-2004 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------

s = sin( rem(a,360)*pi/180 );


