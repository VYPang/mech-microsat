function z = ACosD( x )

%% Inverse cosine with output in degrees.
%
%   Since version 1.
%--------------------------------------------------------------------------
%   Form:
%   z = ACosD( x )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   x                  Argument (deg)
%
%   -------
%   Outputs
%   -------
%   z                  acos(x) (deg)
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%	 Copyright (c) 1996 Princeton Satellite Systems, Inc. 
%    All rights reserved.
%--------------------------------------------------------------------------

z = (180/pi)*acos( x );

