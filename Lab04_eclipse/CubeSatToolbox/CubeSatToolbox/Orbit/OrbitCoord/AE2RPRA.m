function [rP, rA] = AE2RPRA( a, e )

%% Converts semi-major axis and eccentricity to right perigee and apogee.
%--------------------------------------------------------------------------
%   Form:
%   [rP, rA] = AE2RPRA( a, e )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   a               (1,1) Semi-major axis
%   e               (1,1) Eccentricity
%
%   -------
%   Outputs
%   -------
%   rP              (1,1) Perigee
%   rA              (1,1) Apogee
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%	  References:	  Bate, R.R., et. al. Fundamentals of Astrodynamics, pp. 70-71.
%--------------------------------------------------------------------------
%   Copyright 1993-2000 Princeton Satellite Systems, Inc.
%   All rights reserved.
%--------------------------------------------------------------------------

rP   = a*(1-e);
rA   = a*(1+e);


