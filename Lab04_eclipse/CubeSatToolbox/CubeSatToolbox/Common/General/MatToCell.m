function c = MatToCell( m )

%% Converts a string matrix to a cell array.
%--------------------------------------------------------------------------
%   Form:
%   c = MatToCell( m )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   m         (:)   String matrix
%
%   -------
%   Outputs
%   -------
%   c	        {}    One dimensional cell array of strings
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%	  Copyright (c) 2000 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------

if( isempty(m) )
  c = {};
else
  for k = 1:size(m,1)
    c{k} = m(k,:);
  end
end


