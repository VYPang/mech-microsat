function x = IsValidField( m, s )

%% Determine if a field exists and is not empty.
%
%--------------------------------------------------------------------------
%   Form:
%   x = IsValidField( m, s )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%
%   m     (1,1) Structure
%   s     (1,:) Field
%
%   -------
%   Outputs
%   -------
%   x     (1,1)  1 = true
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright 1998 Princeton Satellite Systems, Inc.
%   All rights reserved.
%--------------------------------------------------------------------------
%   Since version 2.

if( isfield( m, s ) && ~isempty( eval(['m.' s]) ) )
  x = 1;
else
  x = 0;
end

