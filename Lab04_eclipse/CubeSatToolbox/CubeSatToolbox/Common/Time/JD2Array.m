function jD = JD2Array( jD0, t )

%% Compute an array of Julian dates from an epoch.
%
%--------------------------------------------------------------------------
%   Forms:
%   jD = JD2Array( jD0, t )
%--------------------------------------------------------------------------
%
%   -----
%   Input
%   -----
%   jD0   (1,1)    Epoch Julian date
%   t     (1,:)    Time array (sec)
%
%   ------
%   Output
%   ------
%   jD    (1,:)    Julian date array
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright (c) 2015 Princeton Satellite Systems, Inc.
%   All Rights Reserved.
%--------------------------------------------------------------------------
%   Since version 2015.1
%--------------------------------------------------------------------------

jD = jD0 + t/86400;


