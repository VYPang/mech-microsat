function qu = QUnit( q )

%% Unitizes a quaternion. 
% Quaternions are assumed to be the columns of q.
%
%--------------------------------------------------------------------------
%   Form:
%   qu = QUnit( q )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   q               (4,:)  Quaternions
%
%   -------
%   Outputs
%   -------
%   qu              (4,:)  Unitized quaternions
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright (c) 1993 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------
%   Since version 1.
%--------------------------------------------------------------------------

c = size(q,2);

if( c > 1 )
  qMag = Mag(q);
  qu   = q./DupVect(qMag,4);
else
  qu   = q/Mag(q);
end

