% The first element is always positive.
%--------------------------------------------------------------------------
%   Form:
%   q = QRand
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   None
%
%   -------
%   Outputs
%   -------
%   q         (4,1)   A random quaternion.    
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright (c) 1999 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------
%   Since 1
%--------------------------------------------------------------------------

q = QUnit( [rand(1,1); 2*(rand(3,1) - 0.5)] );

