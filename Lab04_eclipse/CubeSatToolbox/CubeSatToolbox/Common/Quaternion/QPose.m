function qp = QPose( q )
	
%% Transpose of a quaternion.
%
%--------------------------------------------------------------------------
%   Form:
%   qp = QPose( q )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   q                   (4,:)  quaternion from a to b
%
%   -------
%   Outputs
%   -------
%   qp                  (4,:)  quaternion from b to a
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright (c) 1993 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------
%   Since version 1
%--------------------------------------------------------------------------

qp(1,:)   =   q(1,:);
qp(2:4,:) = - q(2:4,:);

