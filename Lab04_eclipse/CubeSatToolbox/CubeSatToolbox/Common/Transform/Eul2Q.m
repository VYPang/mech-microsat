function q = Eul2Q( e )	
	
%% Converts 3-2-1 Euler angles to a quaternion
% Uses the half angle formulas. 
%
%--------------------------------------------------------------------------
%   Form:
%   q = Eul2Q( e )
%--------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   e		(3,:)    Euler angles (rad)
%
%   -------
%   Outputs
%   -------
%   q		(4,:)    Equivalent quaternion
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%   Copyright (c) 1994 Princeton Satellite Systems, Inc. 
%   All rights reserved.
%--------------------------------------------------------------------------
%   Since version 1.
%--------------------------------------------------------------------------

c = cos(0.5*e);
s = sin(0.5*e);

q = [ c(1,:).*c(2,:).*c(3,:) + s(1,:).*s(2,:).*s(3,:);...
      c(1,:).*s(2,:).*s(3,:) - s(1,:).*c(2,:).*c(3,:);...
     -s(1,:).*c(2,:).*s(3,:) - c(1,:).*s(2,:).*c(3,:);...
      s(1,:).*s(2,:).*c(3,:) - c(1,:).*c(2,:).*s(3,:)];

